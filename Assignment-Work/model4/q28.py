# # • What relationship is appropriate for Student and Person?





# Scenario: Student and Person

# Person → represents a generic person with attributes like name, age, gender, etc.

# Student → represents a specific type of person who is enrolled in a school or college.



# Appropriate Relationship

# A Student is a Person.

# This is an “is-a” relationship, which corresponds to Inheritance in OOP.

# Reason:

# A student shares all attributes and behaviors of a person (name, age, etc.)
#  but may also have additional attributes like roll_number, courses, or grades.